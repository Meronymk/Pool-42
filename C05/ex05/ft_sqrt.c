int	ft_sqrt(int nb)
{
	int	i;

	if (nb > 0)
	{
		i = 1;
		while (i * i <= nb)
		{
			if ((i * i) == nb)
				return (i);
			else if (i > 46340)
				return (0);
			i++;
		}
	}
	return (0);
}
