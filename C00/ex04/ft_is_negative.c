#include <unistd.h>

void	ft_is_negative(int n)
{
	char	nn;
	char	pp;

	nn = 'N';
	pp = 'P';
	if (n >= 0)
	{
		write(1, &pp, 1);
	}
	else
	{
		write(1, &nn, 1);
	}
}
